void main() {
  // Sử dụng các kiểu dữ liệu trong Dart
  int myInt = 10;
  double myDouble = 3.14;
  bool myBool = true;
  String myString = "Hello, world!";
  List<int> myList = [1, 2, 3];
  Map<String, int> myMap = {"apple": 1, "banana": 2, "orange": 3};
  Set<int> mySet = {1, 2, 3};

  // In ra giá trị của các biến
  print('myInt: $myInt');
  print('myDouble: $myDouble');
  print('myBool: $myBool');
  print('myString: $myString');
  print('myList: $myList');
  print('myMap: $myMap');
  print('mySet: $mySet');
}